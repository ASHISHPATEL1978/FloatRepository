package datatypefloat;

// Modified with comment

/* Block Comment Added */

public class DataTypeFloat
{
    public static void main ( String[] args )
    {
        float A = 20;
        float B = 4;

        System.out.println ( A + B );
        System.out.println ( A - B );
        System.out.println ( A * B );
        System.out.println ( A / B );
        System.out.println ( A++ );
        System.out.println ( B-- );
        System.out.println ( ++A + --B );
        System.out.println ( ++A - --B );
    }
}



